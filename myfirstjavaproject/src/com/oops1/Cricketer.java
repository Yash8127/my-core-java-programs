package com.oops1;

//DTO: data transfer object
//POJO:plain old java object

public class Cricketer {

	private int jno;
	private String name;
	private int age;

	public void setJno(int jno) {
		this.jno = jno;
	}

	public int getJno() {
		return jno;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setAge(int age) {
		this.age = age;

	}

	public int getAge() {
		return age;
	}

}
